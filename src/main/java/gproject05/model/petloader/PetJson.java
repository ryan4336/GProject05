package gproject05.model.petloader;

public class PetJson {
	private int id;
    private String name;
    private String species;
    private int age;
    private boolean adopted;
    private String type;
    
	public int getId() {
		return id;
	}
	public String getName() {
		return name;
	}
	public String getSpecies() {
		return species;
	}
	public int getAge() {
		return age;
	}
	public boolean isAdopted() {
		return adopted;
	}
	public String getType() {
		return type;
	}
    
}
