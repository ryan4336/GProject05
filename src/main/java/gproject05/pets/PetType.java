package gproject05.pets;
/**
 * Represents the types of pets.
 * <p>
 * This enum defines the different types of pets that can exist in the system:
 * DOG, CAT, RABBIT, and EXOTIC. Each type corresponds to a category of pet.
 * </p>
 */
public enum PetType {
	/**
     * Represents a dog type pet.
     */
	DOG,
	/**
     * Represents a cat type pet.
     */
	CAT,
	/**
     * Represents a rabbit type pet.
     */
	RABBIT,
	/**
     * Represents an exotic animal type pet.
     */
	EXOTIC
}
